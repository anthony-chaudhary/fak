//go:build amd64

#include "textflag.h"

// func fdot3asm(r0, r1, r2, x *float32, n int) (a, b, c float32)
TEXT ·fdot3asm(SB), NOSPLIT, $96-52
	MOVQ r0+0(FP), DI
	MOVQ r1+8(FP), SI
	MOVQ r2+16(FP), DX
	MOVQ x+24(FP), R8
	MOVQ n+32(FP), CX
	VXORPS Y0, Y0, Y0
	VXORPS Y1, Y1, Y1
	VXORPS Y2, Y2, Y2

	CMPQ CX, $8
	JL   reduce

loop:
	VMOVUPS (R8), Y3

	VMOVUPS (DI), Y4
	VFMADD231PS Y3, Y4, Y0

	VMOVUPS (SI), Y5
	VFMADD231PS Y3, Y5, Y1

	VMOVUPS (DX), Y6
	VFMADD231PS Y3, Y6, Y2

	ADDQ $32, DI
	ADDQ $32, SI
	ADDQ $32, DX
	ADDQ $32, R8
	SUBQ $8, CX
	CMPQ CX, $8
	JGE  loop

reduce:
	VMOVUPS Y0, 0(SP)
	VMOVUPS Y1, 32(SP)
	VMOVUPS Y2, 64(SP)

	VMOVSS 0(SP), X0
	VMOVSS 4(SP), X3
	VADDSS X3, X0, X0
	VMOVSS 8(SP), X3
	VMOVSS 12(SP), X4
	VADDSS X4, X3, X3
	VADDSS X3, X0, X0
	VMOVSS 16(SP), X3
	VMOVSS 20(SP), X4
	VADDSS X4, X3, X3
	VMOVSS 24(SP), X4
	VMOVSS 28(SP), X5
	VADDSS X5, X4, X4
	VADDSS X4, X3, X3
	VADDSS X3, X0, X0

	VMOVSS 32(SP), X1
	VMOVSS 36(SP), X3
	VADDSS X3, X1, X1
	VMOVSS 40(SP), X3
	VMOVSS 44(SP), X4
	VADDSS X4, X3, X3
	VADDSS X3, X1, X1
	VMOVSS 48(SP), X3
	VMOVSS 52(SP), X4
	VADDSS X4, X3, X3
	VMOVSS 56(SP), X4
	VMOVSS 60(SP), X5
	VADDSS X5, X4, X4
	VADDSS X4, X3, X3
	VADDSS X3, X1, X1

	VMOVSS 64(SP), X2
	VMOVSS 68(SP), X3
	VADDSS X3, X2, X2
	VMOVSS 72(SP), X3
	VMOVSS 76(SP), X4
	VADDSS X4, X3, X3
	VADDSS X3, X2, X2
	VMOVSS 80(SP), X3
	VMOVSS 84(SP), X4
	VADDSS X4, X3, X3
	VMOVSS 88(SP), X4
	VMOVSS 92(SP), X5
	VADDSS X5, X4, X4
	VADDSS X4, X3, X3
	VADDSS X3, X2, X2

	TESTQ CX, CX
	JE    done

tail:
	VMOVSS (R8), X3

	VMOVSS (DI), X4
	VFMADD231SS X3, X4, X0

	VMOVSS (SI), X5
	VFMADD231SS X3, X5, X1

	VMOVSS (DX), X6
	VFMADD231SS X3, X6, X2

	ADDQ $4, DI
	ADDQ $4, SI
	ADDQ $4, DX
	ADDQ $4, R8
	DECQ CX
	JNZ  tail

done:
	VMOVSS X0, a+40(FP)
	VMOVSS X1, b+44(FP)
	VMOVSS X2, c+48(FP)
	VZEROUPPER
	RET

// func fdot3asm512(r0, r1, r2, x *float32, n int) (a, b, c float32)
TEXT ·fdot3asm512(SB), NOSPLIT, $0-52
	MOVQ r0+0(FP), DI
	MOVQ r1+8(FP), SI
	MOVQ r2+16(FP), DX
	MOVQ x+24(FP), R8
	MOVQ n+32(FP), CX
	VXORPS X0, X0, X0
	VXORPS X1, X1, X1
	VXORPS X2, X2, X2

	CMPQ CX, $16
	JL   reduce512

loop512:
	VMOVUPS (R8), Z3

	VMOVUPS (DI), Z4
	VFMADD231PS Z3, Z4, Z0

	VMOVUPS (SI), Z5
	VFMADD231PS Z3, Z5, Z1

	VMOVUPS (DX), Z6
	VFMADD231PS Z3, Z6, Z2

	ADDQ $64, DI
	ADDQ $64, SI
	ADDQ $64, DX
	ADDQ $64, R8
	SUBQ $16, CX
	CMPQ CX, $16
	JGE  loop512

reduce512:
	VEXTRACTI64X4 $1, Z0, Y31
	VADDPS        Y31, Y0, Y0
	VEXTRACTI32X4 $1, Z0, X31
	VADDPS        X31, X0, X0
	VPSHUFD       $0xEE, X0, X31
	VADDPS        X31, X0, X0
	VPSHUFD       $0x55, X0, X31
	VADDSS        X31, X0, X0

	VEXTRACTI64X4 $1, Z1, Y31
	VADDPS        Y31, Y1, Y1
	VEXTRACTI32X4 $1, Z1, X31
	VADDPS        X31, X1, X1
	VPSHUFD       $0xEE, X1, X31
	VADDPS        X31, X1, X1
	VPSHUFD       $0x55, X1, X31
	VADDSS        X31, X1, X1

	VEXTRACTI64X4 $1, Z2, Y31
	VADDPS        Y31, Y2, Y2
	VEXTRACTI32X4 $1, Z2, X31
	VADDPS        X31, X2, X2
	VPSHUFD       $0xEE, X2, X31
	VADDPS        X31, X2, X2
	VPSHUFD       $0x55, X2, X31
	VADDSS        X31, X2, X2

	TESTQ CX, CX
	JE    done512

tail512:
	VMOVSS (R8), X3

	VMOVSS (DI), X4
	VFMADD231SS X3, X4, X0

	VMOVSS (SI), X5
	VFMADD231SS X3, X5, X1

	VMOVSS (DX), X6
	VFMADD231SS X3, X6, X2

	ADDQ $4, DI
	ADDQ $4, SI
	ADDQ $4, DX
	ADDQ $4, R8
	DECQ CX
	JNZ  tail512

done512:
	VMOVSS X0, a+40(FP)
	VMOVSS X1, b+44(FP)
	VMOVSS X2, c+48(FP)
	VZEROUPPER
	RET
