func TestSignature(t *testing.T) {
	a := Signature("proj", "/cwd", "do the thing")
	b := Signature("proj", "/cwd", "do the thing")
	c := Signature("proj", "/cwd", "do a DIFFERENT thing")
	if a == "" || len(a) != 16 {
		t.Fatalf("signature=%q, want 16 hex", a)
	}
	if a != b {
		t.Fatalf("signature not deterministic: %q != %q", a, b)
	}
	if a == c {
		t.Fatalf("different directives produced same signature %q", a)
	}
	if got := Signature("proj", "/cwd", ""); got != "" {
		t.Fatalf("empty directive signature=%q", got)
	}
}

func TestDistinctGoalsDedupToN(t *testing.T) {
	goals := []string{
		"model routing first class", "progress epic 595", "GLM multi-GPU NCCL",
		"vllm comparisons", "generic account switcher", "context tape visuals",
		"dispatch loop watchdog", "hosted control plane", "industry scorecard",
		"persona readiness", "agent readiness", "seo aeo scorecard",
		"trajectory garden", "repo hygiene", "memory compaction",
	}
	sigs := map[string]bool{}
	for _, goal := range goals {
		_, sig := Identity("proj", "/cwd", slashGoalHead(goal))
		if sig == "" {
			t.Fatalf("empty signature for goal %q", goal)
		}
		sigs[sig] = true
	}
	if len(sigs) != len(goals) {
		t.Fatalf("distinct signatures=%d, want %d", len(sigs), len(goals))
	}
}
