def build_site_schemas() -> list[dict]:
    url = site_url()
    desc = site_description()
    img = social_image()
    name = "Netra Fused Agent Kernel (`fak`)"
    publisher = {
        "@type": "Organization",
        "name": "Netra Systems",
        "url": url,
        "logo": img,
        "sameAs": [REPO_URL],
    }
    software = {
        "@context": "https://schema.org",
        "@type": "SoftwareApplication",
        "name": "Netra Fused Agent Kernel",
        "alternateName": ["fak", "Fused Agent Kernel", "agent tool firewall"],
        "description": desc,
        "applicationCategory": "DeveloperApplication",
        "applicationSubCategory": "SecurityApplication",
        "operatingSystem": "Linux, macOS, Windows",
        "programmingLanguage": "Go",
        "softwareVersion": version(),
        "license": "https://www.apache.org/licenses/LICENSE-2.0",
        "codeRepository": REPO_URL,
        "url": url,
        "image": img,
        "downloadUrl": REPO_URL,
        "author": {"@type": "Person", "name": "Anthony Chaudhary"},
        "publisher": publisher,
        "offers": {"@type": "Offer", "price": "0", "priceCurrency": "USD"},
        "keywords": ("agent kernel, agent tool firewall, AI agent security, prompt injection "
                     "defense, tool poisoning, capability security, default-deny permission gate, "
                     "KV cache, addressable KV cache, self-hosted LLM, LLM agent fleet, Go"),
    }
    website = {
        "@context": "https://schema.org",
        "@type": "WebSite",
        "name": name,
        "url": url,
        "description": desc,
        "inLanguage": "en",
        "publisher": publisher,
    }
    organization = {
        "@context": "https://schema.org",
        **publisher,
    }
    # NB: no global BreadcrumbList. A breadcrumb injected into EVERY page's <head>
    # would claim the same degenerate Home->Home trail everywhere (the review's nit);
    # a real breadcrumb is per-page hierarchy, which this site's flat docs root lacks.
    return [software, website, organization]


def build_faqpage(faq_md: str) -> dict | None:
    pairs = faq_pairs(faq_md)
    if not pairs:
        return None
    return {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": [
            {"@type": "Question", "name": q,
             "acceptedAnswer": {"@type": "Answer", "text": a}}
            for q, a in pairs
        ],
    }


def build_index_breadcrumb() -> dict:
    url = site_url()
    parent = url.rsplit("/", 1)[0] + "/" if "/" in url else url
    return {
        "@context": "https://schema.org",
        "@type": "BreadcrumbList",
        "itemListElement": [
            {
                "@type": "ListItem",
                "position": 1,
                "name": "Home",
                "item": parent,
            },
            {
                "@type": "ListItem",
                "position": 2,
                "name": "fak documentation",
                "item": url + "/",
            },
        ],
    }


def faqpage_artifacts(faq_md: str) -> list[str]:
    """Answer texts that still carry raw markdown markers (*, ```, ``, ](). An
    empty list means the flattener produced clean prose — the guard the drift
    check ('--check') cannot provide on its own, since corruption baked into the
    generator is byte-stable against itself."""
    schema = build_faqpage(faq_md)
    if not schema:
        return []
    bad = []
    for q in schema["mainEntity"]:
        a = q["acceptedAnswer"]["text"]
        if _ARTIFACT_RE.search(a):
            bad.append(f"{q['name']!r}: {a[:80]}…")
    return bad


def _script(obj: dict) -> str:
    payload = json.dumps(obj, ensure_ascii=False, indent=2)
    return f'<script type="application/ld+json">\n{payload}\n</script>'


def render_site_block() -> str:
    blocks = "\n".join(_script(s) for s in build_site_schemas())
    return f"{SITE_BEGIN}\n{blocks}\n{SITE_END}"


def render_faq_block(faq_md: str) -> str | None:
    schema = build_faqpage(faq_md)
    if schema is None:
        return None
    return f"{FAQ_BEGIN}\n{_script(schema)}\n{FAQ_END}"


def render_breadcrumb_block() -> str:
    return f"{BREADCRUMB_BEGIN}\n{_script(build_index_breadcrumb())}\n{BREADCRUMB_END}"


# ---------------------------------------------------------------------------
# Idempotent in-place injection
# ---------------------------------------------------------------------------

def render_whatsnew_block(cap: int = 12) -> str:
    """Render the llms.txt "What's new" block from docs/marketing/updates.json.

    The feed is a schema.org ItemList of witnessed ships (written by `fak marketing aeo`);
    each item's `item.name` is the claim and `item.codeRepository` is the witnessing commit
    URL. We render a bounded, dated markdown list under a `## What's new` heading, every line
    citing its commit — the recency surface an answer engine quotes. Returns "" (the block is
    then omitted) when the feed is absent or empty, so a repo without the marketing loop wired
    simply has no What's-new section rather than a broken one."""
    raw = _read(UPDATES_FEED)
    if not raw.strip():
        return ""
    try:
        feed = json.loads(raw)
    except json.JSONDecodeError:
        return ""
    items = feed.get("itemListElement", [])
    if not items:
        return ""
    lines = [WHATSNEW_BEGIN, "", "## What's new", ""]
    for el in items[:cap]:
        it = el.get("item", {})
        name = (it.get("name") or "").strip()
        url = (it.get("codeRepository") or "").strip()
        date = (it.get("dateModified") or "").strip()
        if not name:
            continue
        sha = url.rsplit("/", 1)[-1] if url else ""
        stamp = f"**{date}** — " if date else ""
        cite = f" ([`{sha}`]({url}))" if url else ""
        lines.append(f"- {stamp}{name}{cite}")
    lines += ["", WHATSNEW_END]
    return "\n".join(lines)


def inject(text: str, begin: str, end: str, block: str, *, after_front_matter: bool) -> str:
    """Replace an existing BEGIN..END block, else insert a fresh one.

    For a markdown page (`after_front_matter`), a new block lands right after the
    `---` front-matter fence (so front matter stays line 1) — else after the H1,
    else at the top. For an HTML include it is appended."""
    pat = re.compile(re.escape(begin) + r".*?" + re.escape(end), re.DOTALL)
    if pat.search(text):
        return pat.sub(lambda _m: block, text)
    if not after_front_matter:
        sep = "" if text.endswith("\n") or not text else "\n"
        return text + sep + block + "\n"
    # markdown: after front matter if present
    if text.startswith("---"):
        fm_end = text.find("\n---", 3)
        if fm_end != -1:
            cut = text.find("\n", fm_end + 1)
            cut = cut + 1 if cut != -1 else len(text)
            return text[:cut] + "\n" + block + "\n" + text[cut:]
    # else after the first H1
    m = re.search(r"^#\s+.*$", text, re.MULTILINE)
    if m:
        cut = m.end()
        nl = text.find("\n", cut)
        cut = nl + 1 if nl != -1 else len(text)
        return text[:cut] + "\n" + block + "\n" + text[cut:]
    return block + "\n\n" + text


def append_block(text: str, begin: str, end: str, block: str) -> str:
    """Replace an existing BEGIN..END block, but keep the generated block out of
    the first-screen prose by appending it to the document."""
    pat = re.compile(re.escape(begin) + r".*?" + re.escape(end), re.DOTALL)
    body = pat.sub("", text).rstrip()
    sep = "\n\n" if body else ""
    return body + sep + block + "\n"


def _write(path: str, text: str) -> None:
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(text)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description="Generate JSON-LD structured data (read facts, write blocks).")
    ap.add_argument("--check", action="store_true", help="exit 1 if any block is out of date (CI)")
    args = ap.parse_args(argv)

    head = _read(HEAD_INCLUDE)
    faq = _read(FAQ)
    index = _read(INDEX_PAGE)
    llms = _read(LLMS)

    new_head = inject(head, SITE_BEGIN, SITE_END, render_site_block(), after_front_matter=False)
    faq_block = render_faq_block(faq)
    new_faq = inject(faq, FAQ_BEGIN, FAQ_END, faq_block, after_front_matter=True) if faq_block else faq
    new_index = append_block(index, BREADCRUMB_BEGIN, BREADCRUMB_END, render_breadcrumb_block())
    # llms.txt "What's new": inject the recency block from the marketing feed after the H1,
    # touching only the marked span (the hand-written body is preserved). When the feed is
    # absent/empty the block is "" and any existing marked span is left as-is.
    whatsnew = render_whatsnew_block()
    new_llms = inject(llms, WHATSNEW_BEGIN, WHATSNEW_END, whatsnew, after_front_matter=True) if (llms and whatsnew) else llms

    changed = []
    if new_head != head:
        changed.append("docs/_includes/head-custom.html")
    if new_faq != faq:
        changed.append("docs/FAQ.md")
    if new_index != index:
        changed.append("docs/index.md")
    if new_llms != llms:
        changed.append("llms.txt")

    # Accuracy guard (independent of drift): the flattened FAQ answers must carry
    # no raw markdown markers — catches a _plain() regression even when the
    # committed output is byte-stable against the (buggy) generator.
    artifacts = faqpage_artifacts(faq)

    if args.check:
        rc = 0
        if changed:
            print("structured data OUT OF DATE; run: python tools/gen_structured_data.py")
            for c in changed:
                print(f"  - {c}")
            rc = 1
        if artifacts:
            print("FAQPage answers carry raw markdown artifacts (fix _plain()):")
            for a in artifacts:
                print(f"  - {a}")
            rc = 1
        if rc == 0:
            print("structured data up to date (no drift, no answer artifacts)")
        return rc

    if new_head != head:
        _write(HEAD_INCLUDE, new_head)
    if faq_block and new_faq != faq:
        _write(FAQ, new_faq)
    if new_index != index:
        _write(INDEX_PAGE, new_index)
    if new_llms != llms:
        _write(LLMS, new_llms)

    n_faq = len(build_faqpage(faq).get("mainEntity", [])) if faq_block else 0
    print(f"wrote site JSON-LD ({len(build_site_schemas())} schemas) -> docs/_includes/head-custom.html")
    print(f"wrote FAQPage JSON-LD ({n_faq} Q&A) -> docs/FAQ.md")
    print("wrote BreadcrumbList JSON-LD -> docs/index.md")
    if new_llms != llms:
        print("wrote What's-new block -> llms.txt")
    if not changed:
        print("(no changes — already up to date)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
