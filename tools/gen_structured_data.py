#!/usr/bin/env python3
"""Generate JSON-LD structured data for the docs site — the AEO heavy-hitter.

Answer engines (ChatGPT, Perplexity, Google AI Overviews) and rich-result crawlers
read JSON-LD to identify a project and cite it correctly. The fak docs site shipped
ZERO structured data; this generator emits it, re-derived from repo facts so it can
never drift from reality (the same discipline as `tools/gen_llms_full.py` and the
docs scorecard — no hand-entry):

  SITE-WIDE  -> injected between markers in docs/_includes/head-custom.html, so the
               cayman layout's head hook puts it on every page:
    SoftwareApplication   what the project IS (category, language, license, version)
    WebSite               the site identity (name, url, publisher, language)
    Organization          the publisher (logo, sameAs the GitHub repo)

  INDEX PAGE -> injected between markers in docs/index.md, where the visible page is
               the project documentation front door:
    BreadcrumbList        a truthful Home -> fak documentation trail

  FAQ-PAGE   -> injected between markers in docs/FAQ.md (FAQPage markup must live on
               the page whose visible Q&A it mirrors, per Google's guideline):
    FAQPage               every `## question` + its answer, from the live FAQ

Facts come from VERSION, docs/_config.yml (url/baseurl/description), and llms.txt.
The injected blocks are bounded by HTML-comment markers and rewritten in place, so
re-running is idempotent. IMPORTANT: the blocks are pure <script> JSON — never a
Liquid tag — so they are safe to inject into head-custom.html (a Liquid tag there
recurses the Pages build; see that file's header).

Run:  python tools/gen_structured_data.py            # write the blocks
      python tools/gen_structured_data.py --check     # CI: fail if out of date
"""
from __future__ import annotations

import argparse
import json
import os
import re

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
CONFIG = os.path.join(ROOT, "docs", "_config.yml")
LLMS = os.path.join(ROOT, "llms.txt")
FAQ = os.path.join(ROOT, "docs", "FAQ.md")
HEAD_INCLUDE = os.path.join(ROOT, "docs", "_includes", "head-custom.html")
INDEX_PAGE = os.path.join(ROOT, "docs", "index.md")
VERSION_FILE = os.path.join(ROOT, "VERSION")

UPDATES_FEED = os.path.join(ROOT, "docs", "marketing", "updates.json")

REPO_URL = "https://github.com/anthony-chaudhary/fak"
SITE_BEGIN = "<!-- STRUCTURED-DATA:BEGIN (generated by tools/gen_structured_data.py — do not edit by hand) -->"
SITE_END = "<!-- STRUCTURED-DATA:END -->"
# The recency block fenced into llms.txt from the marketing updates feed (docs/marketing/
# updates.json, written by `fak marketing aeo`). It fills the gap llms.txt has no "what's
# new" section, so an answer engine answering "what is the latest in fak" has a dated,
# sha-cited block to cite. The feed is GENERATED (Go, from witnessed ships); this injects it
# between markers so the hand-written body of llms.txt is never touched.
WHATSNEW_BEGIN = "<!-- WHATS-NEW:BEGIN (generated from docs/marketing/updates.json by `fak marketing aeo` — do not edit by hand) -->"
WHATSNEW_END = "<!-- WHATS-NEW:END -->"
FAQ_BEGIN = "<!-- FAQPAGE-JSONLD:BEGIN (generated by tools/gen_structured_data.py — do not edit by hand) -->"
FAQ_END = "<!-- FAQPAGE-JSONLD:END -->"
BREADCRUMB_BEGIN = "<!-- BREADCRUMB-JSONLD:BEGIN (generated by tools/gen_structured_data.py — do not edit by hand) -->"
BREADCRUMB_END = "<!-- BREADCRUMB-JSONLD:END -->"


def _read(path: str) -> str:
    try:
        with open(path, encoding="utf-8") as f:
            return f.read()
    except OSError:
        return ""


def version() -> str:
    return (_read(VERSION_FILE).strip() or "unknown")


def site_url() -> str:
    cfg = _read(CONFIG)
    url = re.search(r'^url:\s*"?([^"\n]+)', cfg, re.MULTILINE)
    base = re.search(r'^baseurl:\s*"?([^"\n]+)', cfg, re.MULTILINE)
    u = (url.group(1).strip() if url else "https://anthony-chaudhary.github.io").rstrip("/")
    b = (base.group(1).strip() if base else "/fak")
    return u + b


def site_description() -> str:
    cfg = _read(CONFIG)
    # The folded `description: >-` block in _config.yml.
    m = re.search(r"^description:\s*>-\s*\n((?:[ \t]+.*\n?)+)", cfg, re.MULTILINE)
    if m:
        return " ".join(line.strip() for line in m.group(1).splitlines() if line.strip())
    return ("Netra Fused Agent Kernel (`fak`): an in-process, default-deny permission "
            "gate for AI agents fused with an addressable, bit-exact KV cache, in Go.")


def social_image() -> str:
    cfg = _read(CONFIG)
    m = re.search(r'image:\s*"?([^"\n]+)', cfg)
    return m.group(1).strip() if m else (
        "https://raw.githubusercontent.com/anthony-chaudhary/fak/main/visuals/social-preview.png")


# ---------------------------------------------------------------------------
# FAQ extraction (-> FAQPage)
# ---------------------------------------------------------------------------

_MD_LINK = re.compile(r"\[([^\]]+)\]\([^)]+\)")
_MD_FENCE = re.compile(r"```.*?```", re.DOTALL)   # a fenced code block (multi-line)
_MD_BOLD = re.compile(r"\*\*([^*]+)\*\*")          # **bold**
_MD_EMPH = re.compile(r"(?<!\*)\*([^*]+)\*(?!\*)")  # *italic* (run AFTER whitespace-collapse)
# underscore emphasis only at word boundaries — never inside snake_case identifiers.
_MD_UND = re.compile(r"(?<![A-Za-z0-9_])_([^_]+)_(?![A-Za-z0-9_])")
_MD_CODE = re.compile(r"`([^`]+)`")                # `inline code`
# A leading markdown MARKER: a blockquote/heading run, a bullet, or an `N.` list
# number — each REQUIRES a trailing space, so a bare leading digit (e.g. "4x fewer
# tokens" after **bold** is stripped) is NEVER mistaken for a list marker. This is
# the bug the adversarial review caught: the old `^[>#\-\*\d\.]+` ate the "4" from
# "4x", silently corrupting the headline benchmark in the machine-readable answer.
_MD_LEAD = re.compile(r"^\s*(?:[>#]+|[-*+]|\d+\.)\s+", re.MULTILINE)


def _plain(text: str) -> str:
    """Flatten markdown answer prose to a clean plain-text JSON-LD answer string.

    Order matters:
      1. drop fenced code blocks (a flattened multi-line command reads as noise);
      2. strip leading list/quote/heading MARKERS while line starts still exist;
      3. collapse all whitespace to single spaces — this also JOINS emphasis that
         wraps across a source line break (`*same call\\npath*`), which a per-line
         regex could never match (the bug the review's artifact guard caught);
      4. only now strip inline markdown — bold BEFORE single-* emphasis so `**x**`
         is consumed before `*x*`, then underscore emphasis, then inline code.
    """
    text = _MD_FENCE.sub(" ", text)
    text = _MD_LEAD.sub("", text)
    text = re.sub(r"\s+", " ", text).strip()
    text = _MD_LINK.sub(r"\1", text)
    text = _MD_BOLD.sub(r"\1", text)
    text = _MD_EMPH.sub(r"\1", text)
    text = _MD_UND.sub(r"\1", text)
    text = _MD_CODE.sub(r"\1", text)
    return text.strip()


# Stray markdown markers that must NOT survive into a JSON-LD answer (the artifact
# guard the review asked for, so --check can catch a regression the drift check can't).
_ARTIFACT_RE = re.compile(r"\*|```|`{2,}|\]\(")


def faq_pairs(faq_md: str) -> list[tuple[str, str]]:
    """(question, answer-plain-text) for every `## …` section in FAQ.md.

    A heading that does not read as a question (no '?', and not a 'How/What/Why/…'
    lead) is skipped — keeps section dividers out of the FAQPage."""
    body = faq_md
    # Drop front matter if present.
    if body.startswith("---"):
        end = body.find("\n---", 3)
        if end != -1:
            body = body[end + 4:]
    parts = re.split(r"^##\s+(.+)$", body, flags=re.MULTILINE)
    # parts = [pre, q1, a1, q2, a2, ...]
    out: list[tuple[str, str]] = []
    for i in range(1, len(parts), 2):
        q = parts[i].strip()
        a_raw = parts[i + 1] if i + 1 < len(parts) else ""
        # Stop the answer at a horizontal rule if one separates sections.
        a_raw = re.split(r"^---\s*$", a_raw, flags=re.MULTILINE)[0]
        a = _plain(a_raw)
        is_question = q.endswith("?") or re.match(
            r"(?i)^(how|what|why|when|where|who|which|is|are|can|does|do|should|will)\b", q)
        if not is_question or len(a) < 20:
            continue
        # Trim absurdly long answers (answer engines want the gist).
        if len(a) > 1200:
            a = a[:1190].rsplit(" ", 1)[0] + " …"
        out.append((q, a))
    return out


