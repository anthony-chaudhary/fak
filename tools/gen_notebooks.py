"""Single source of truth for the cloud-notebook quickstarts under notebooks/.

The notebooks are GENERATED ARTIFACTS — do not hand-edit the .ipynb files. Edit the
shared cell builders here, then regenerate:

    python tools/gen_notebooks.py            # (re)write notebooks/*.ipynb
    python tools/gen_notebooks.py --check    # CI: fail on drift OR a stale repo reference

Why a generator instead of two hand-kept notebooks:
  - DRY: the setup + get-the-binary cells are IDENTICAL across notebooks; they live once,
    here, so a change (a new flag, a pinned version, a clone tweak) updates every notebook.
  - Anti-rot: --check re-renders in memory and diffs against the committed files (a
    "generated, do not edit" guard, like a gofmt check), AND verifies every repo path the
    cells reference still exists — so deleting examples/…json or scripts/fetch-model.sh
    fails CI here instead of failing a user mid-notebook.

Modular update path in one line: edit a builder -> run this -> commit; CI runs --check.
"""
