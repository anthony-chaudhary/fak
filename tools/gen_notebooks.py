def notebook(cells):
    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
            "language_info": {"name": "python"},
            "colab": {"provenance": []},
        },
        "nbformat": 4, "nbformat_minor": 5,
    }


TARGETS = {
    "fak-quickstart.ipynb": quickstart_cells,
    "fak-inkernel.ipynb": inkernel_cells,
}


def render(name):
    return json.dumps(notebook(TARGETS[name]()), indent=1, ensure_ascii=True) + "\n"


def verify_refs():
    """Fail if a repo path/verb the cells depend on has gone away."""
    problems = []
    for rel, why in REQUIRED_PATHS:
        if not (ROOT / rel).exists():
            problems.append(f"missing {rel}  ({why})")
    # cmd/fak must still register the verbs the notebooks invoke
    src = "\n".join(p.read_text(encoding="utf-8", errors="ignore")
                    for p in (ROOT / "cmd" / "fak").glob("*.go")) if (ROOT/"cmd"/"fak").is_dir() else ""
    for verb in REQUIRED_VERBS:
        if f'"{verb}"' not in src:
            problems.append(f"verb '{verb}' not found in cmd/fak/*.go (notebook invokes it)")
    return problems


def main(argv):
    check = "--check" in argv
    drift, missing = [], verify_refs()
    for name in TARGETS:
        want = render(name)
        path = NB / name
        if check:
            have = path.read_text(encoding="utf-8") if path.exists() else ""
            if have != want:
                drift.append(name)
        else:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(want, encoding="utf-8")
            print(f"wrote notebooks/{name}  ({len(TARGETS[name]())} cells)")

    if check:
        for name in drift:
            print(f"DRIFT: notebooks/{name} differs from the generator — run "
                  f"`python tools/gen_notebooks.py`", file=sys.stderr)
            have = (NB / name).read_text(encoding="utf-8").splitlines() if (NB/name).exists() else []
            for line in list(difflib.unified_diff(
                    have, render(name).splitlines(), name, name+" (generated)", lineterm=""))[:20]:
                print("  " + line, file=sys.stderr)
        for p in missing:
            print(f"STALE REF: {p}", file=sys.stderr)
        if drift or missing:
            return 1
        print("notebooks in sync with the generator; all referenced repo paths/verbs present.")
    elif missing:
        for p in missing:
            print(f"WARNING stale ref: {p}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
