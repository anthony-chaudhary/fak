#!/usr/bin/env python3
"""
hero_video_gen.py — assemble the fak README hero video from the project's best
existing lab-grade visuals.

The video is a calm, frontier-lab "model card reveal": a montage of the four
headline hero charts (plus the performance spectrum + hardware matrix) with
subtle Ken-Burns motion, gentle cross-dissolves, and house-style title/closing
cards. It is silent, 1920x1080, autoplay-loop friendly.

Pipeline (fully deterministic, no AI footage — the charts ARE the content):
  1. render each storyboard visual SVG -> high-res PNG @2x via headless Chrome
     (falls back to an existing visuals/<name>.png if Chrome is unavailable),
  2. composite the full frame sequence in Pillow (motion on the chart layer only;
     captions stay rock-steady; letterbox is filled with each chart's own
     background colour so it is seamless),
  3. pipe raw RGB frames to the bundled ffmpeg (imageio-ffmpeg) -> H.264 mp4,
     plus a poster PNG and a compact looping GIF preview for README embedding.

Usage:
  python tools/hero_video_gen.py                 # build mp4 + poster + gif
  python tools/hero_video_gen.py --fast          # 15fps draft, quick preview
  python tools/hero_video_gen.py --no-gif        # skip the gif
  python tools/hero_video_gen.py --frames 0,300  # render only a frame range (debug)

Storyboard:  visuals/hero-video.storyboard.json  (generated by the design panel)
Outputs:     visuals/hero-video.mp4 · hero-video-poster.png · hero-video.gif
"""
