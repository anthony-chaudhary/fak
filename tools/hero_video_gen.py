#!/usr/bin/env python3
"""
hero_video_gen.py — assemble the fak README hero video from the project's best
existing lab-grade visuals, with real chart animation.

The video is a calm, frontier-lab "model card reveal": a montage of the headline
hero charts with motion that *builds the chart in front of you* — the turn-tax
curves sweep left-to-right and their big multipliers count up; the dense
receipt cards (stat-card, sweep, capability matrix) wipe in from a steady raster.
It is silent, 2560x1440 by default, autoplay-loop friendly.

Pipeline (fully deterministic, no AI footage — the charts ARE the content):
  1. the load-bearing HERO scene (turn-tax) is drawn frame-by-frame in Pillow
     straight from its source-of-truth data file (tools/hero_turntax.data.json):
     axes are steady, the baseline/fak polylines trace L->R, the gap shades in,
     and the per-panel multiplier (20,480x / 9.7x / 4.1x) counts up. No number
     can drift from the data — the animation reads the same JSON the SVG does.
  2. the dense receipt scenes (stat-card, sweep, capability matrix, spectrum,
     hardware matrix) render each storyboard SVG -> high-res PNG @3x via headless
     Chrome (falls back to an existing visuals/<name>.png), then BUILD IN with a
     left-to-right reveal wipe + a settle, so they feel composed, not pasted.
  3. raw RGB frames are piped to ffmpeg (imageio-ffmpeg) -> H.264 mp4 at a rich
     bitrate (CRF 19), plus a poster PNG and a compact looping GIF teaser.

Usage:
  python tools/hero_video_gen.py                 # build mp4 + poster + gif (2560x1440)
  python tools/hero_video_gen.py --fast          # 15fps draft, quick preview
  python tools/hero_video_gen.py --no-gif        # skip the gif
  python tools/hero_video_gen.py --crf 19        # x264 quality (lower=better/bigger)

Storyboard:  visuals/hero-video.storyboard.json  (generated by the design panel)
Outputs:     visuals/hero-video.mp4 · hero-video-poster.png · hero-video.gif
"""
