CONTENT = (96, 206, 1824, 1002)  # x0,y0,x1,y1 for the chart on visual scenes


def wordmark(draw, cx, y, size):
    """draw 'fak' + a green block cursor, centered on cx, top at y. returns bottom y."""
    fnt = F(MONO_B, size)
    txt = "fak"
    tw, th = measure(draw, txt, fnt)
    cur_w = round(size * 0.16)
    gap = round(size * 0.14)
    total = tw + gap + cur_w
    x0 = cx - total / 2
    draw.text((x0, y), txt, font=fnt, fill=INK)
    asc, desc = fnt.getmetrics()
    # block cursor sits on the text baseline-ish
    cy0 = y + round(size * 0.14)
    cy1 = y + round(size * 0.86)
    cx0 = x0 + tw + gap
    draw.rectangle([cx0, cy0, cx0 + cur_w, cy1], fill=GREEN)
    return y + asc + desc


def plate_visual(scene, chart_bg) -> Image.Image:
    im = Image.new("RGB", (W, H), chart_bg)
    d = ImageDraw.Draw(im)
    cx = W / 2
    # top caption band
    y = 40
    head = F(SEMIBOLD, 50)
    y = draw_center(d, cx, y, scene["headline"], head, INK, max_w=1740)
    if scene.get("subcaption"):
        y += 4
        sub = F(REG, 28)
        y = draw_center(d, cx, y, scene["subcaption"], sub, GREY, max_w=1720)
    # short green accent rule under the caption
    d.rectangle([cx - 34, y + 10, cx + 34, y + 14], fill=GREEN)
    # footer
    if scene.get("footer"):
        ff = F(REG, 22)
        fw = measure(d, scene["footer"], ff)[0]
        d.text((cx - fw / 2, 1024), scene["footer"], font=ff, fill=GREY_L)
    # tiny brand mark, bottom-left
    bm = F(MONO_B, 25)
    d.text((50, 1022), "fak", font=bm, fill=INK)
    bw0 = measure(d, "fak", bm)[0]
    d.rectangle([50 + bw0 + 7, 1026, 50 + bw0 + 15, 1044], fill=GREEN)
    return im


def plate_title(scene) -> Image.Image:
    im = Image.new("RGB", (W, H), CARD_BG)
    d = ImageDraw.Draw(im)
    cx = W / 2
    # big wordmark
    wm_size = 200
    by = wordmark(d, cx, 300, wm_size)
    # subtitle "the agent kernel" (pull from headline after the em dash)
    sub_line = "the agent kernel"
    if "—" in scene["headline"]:
        sub_line = scene["headline"].split("—", 1)[1].strip()
    sf = F(LIGHT, 58)
    swid = measure(d, sub_line, sf)[0]
    d.text((cx - swid / 2, by + 6), sub_line, font=sf, fill=INK2)
    yy = by + 6 + 80
    # accent rule
    d.rectangle([cx - 60, yy + 8, cx + 60, yy + 13], fill=GREEN)
    # tagline (subcaption)
    if scene.get("subcaption"):
        tf = F(REG, 34)
        draw_center(d, cx, yy + 44, scene["subcaption"], tf, GREY, max_w=1280)
    # footer
    if scene.get("footer"):
        ff = F(REG, 24)
        fw = measure(d, scene["footer"], ff)[0]
        d.text((cx - fw / 2, 1004), scene["footer"], font=ff, fill=GREY_L)
    return im


def plate_closing(scene) -> Image.Image:
    im = Image.new("RGB", (W, H), CARD_BG)
    d = ImageDraw.Draw(im)
    cx = W / 2
    by = wordmark(d, cx, 360, 170)
    yy = by + 18
    d.rectangle([cx - 60, yy, cx + 60, yy + 5], fill=GREEN)
    if scene.get("subcaption"):
        tf = F(SEMIBOLD, 40)
        yy2 = draw_center(d, cx, yy + 40, scene["subcaption"], tf, INK2, max_w=1300)
    if scene.get("footer"):
        ff = F(MONO, 30)
        fw = measure(d, scene["footer"], ff)[0]
        d.text((cx - fw / 2, 920), scene["footer"], font=ff, fill=GREEN_D)
    return im


def plate_interstitial(scene) -> Image.Image:
    im = Image.new("RGB", (W, H), CARD_BG)
    d = ImageDraw.Draw(im)
    cx = W / 2
    head = F(SEMIBOLD, 70)
    y = draw_center(d, cx, 348, scene["headline"], head, INK, max_w=1500)
    d.rectangle([cx - 60, y + 18, cx + 60, y + 23], fill=GREEN)
    # the two flips: split the subcaption on the double space into two stacked lines
    sub = scene.get("subcaption") or ""
    parts = [p.strip() for p in sub.split("  ") if p.strip()]
    bf = F(REG, 38)
    yy = y + 70
    for p in parts:
        yy = draw_center(d, cx, yy, p, bf, INK2, max_w=1500) + 14
    if scene.get("footer"):
        ff = F(LIGHT, 30)
        draw_center(d, cx, yy + 10, scene["footer"], ff, GREY, max_w=1400)
    return im


# ---------------------------------------------------------------------------
# Scene model
# ---------------------------------------------------------------------------
class Scene:
    def __init__(self, spec):
        self.spec = spec
        self.kind = spec["kind"]
        self.dur = float(spec["duration_s"])
        self.tin = float(spec["transition_dur_s"])  # cross-dissolve duration
        self.motion = spec["motion"]
        self.intensity = float(spec["motion_intensity"])
        self.chart_bg = CARD_BG
        self.base = None    # contained chart (visual scenes)
        self.box = None     # (x0,y0,bw,bh)
        self.plate = None   # static layer
        self._build()

    def _build(self):
        s = self.spec
        if self.kind == "visual":
            chart = load_visual(s["visual"])
            self.chart_bg = corner_bg(chart)
            chart = autotrim(chart, self.chart_bg)
            x0, y0, x1, y1 = CONTENT
            bw, bh = x1 - x0, y1 - y0
            self.box = (x0, y0, bw, bh)
            self.base = contain(chart, round(bw * 0.998), round(bh * 0.998))
            self.plate = plate_visual(s, self.chart_bg)
        elif self.kind == "title":
            self.plate = plate_title(s)
        elif self.kind == "closing":
            self.plate = plate_closing(s)
        else:
            self.plate = plate_interstitial(s)

    def frame(self, local_t: float) -> Image.Image:
        frac = min(1.0, local_t / self.dur) if self.dur > 0 else 1.0
        if self.kind == "visual":
            im = self.plate.copy()
            x0, y0, bw, bh = self.box
            kb = ken_burns(self.base, bw, bh, frac, self.motion, self.intensity, self.chart_bg)
            im.paste(kb, (x0, y0))
            return im
        else:
            # whole-plate subtle motion for cards
            return ken_burns(self.plate, W, H, frac, self.motion, self.intensity, CARD_BG)


# ---------------------------------------------------------------------------
# Timeline
# ---------------------------------------------------------------------------
def build_timeline(scenes):
    starts = [0.0]
    for i in range(1, len(scenes)):
        starts.append(starts[i - 1] + scenes[i - 1].dur - scenes[i].tin)
    ends = [starts[i] + scenes[i].dur for i in range(len(scenes))]
    return starts, ends


def render(scenes, fps, frange=None):
    starts, ends = build_timeline(scenes)
    total = ends[-1]
    total_frames = math.ceil(total * fps)
    bg0 = Image.new("RGB", (W, H), CARD_BG)

    f_lo, f_hi = 0, total_frames
    if frange:
        f_lo, f_hi = frange

    for f in range(f_lo, f_hi):
        t = f / fps
        # topmost active scene = the one with the largest start <= t and t < end
        cur = 0
        for i in range(len(scenes)):
            if starts[i] <= t < ends[i] and starts[i] >= starts[cur]:
                cur = i
        # clamp to last if past the end
        if t >= ends[-1]:
            cur = len(scenes) - 1
        s = scenes[cur]
        over = s.frame(min(t - starts[cur], s.dur))
        # fade-in compositing
        if cur == 0 and t < s.tin and s.tin > 0:
            a = smoothstep(t / s.tin)
            frame = Image.blend(bg0, over, a)
        elif cur > 0 and t < starts[cur] + s.tin and s.tin > 0:
            prev = scenes[cur - 1]
            under = prev.frame(min(t - starts[cur - 1], prev.dur))
            a = smoothstep((t - starts[cur]) / s.tin)
            frame = Image.blend(under, over, a)
        else:
            frame = over
        frame = draw_progress(frame, t / total)
        yield f, frame


def draw_progress(im, frac):
    d = ImageDraw.Draw(im)
    bw = round(W * max(0.0, min(1.0, frac)))
    # subtle track + green progress along the very bottom edge
    d.rectangle([0, H - 5, W, H], fill=HAIRLINE)
    d.rectangle([0, H - 5, bw, H], fill=GREEN)
    return im


# ---------------------------------------------------------------------------
# Encode
# ---------------------------------------------------------------------------
def ffmpeg_exe():
    import imageio_ffmpeg
    return imageio_ffmpeg.get_ffmpeg_exe()


def write_mp4(scenes, fps, out: Path, want_poster=False, crf=26):
    ff = ffmpeg_exe()
    cmd = [
        ff, "-y", "-f", "rawvideo", "-pix_fmt", "rgb24",
        "-s", f"{W}x{H}", "-r", str(fps), "-i", "-",
        "-an", "-c:v", "libx264", "-pix_fmt", "yuv420p",
        "-crf", str(crf), "-preset", "slow", "-profile:v", "high",
        "-movflags", "+faststart", str(out),
    ]
    proc = subprocess.Popen(cmd, stdin=subprocess.PIPE, stdout=subprocess.DEVNULL,
                            stderr=subprocess.DEVNULL)
    n = 0
    poster_frame = None
    starts, ends = build_timeline(scenes)
    # poster = a readable beat inside the turn-tax scene (the measured 9.7x)
    poster_idx = None
    if want_poster:
        for i, s in enumerate(scenes):
            if s.spec.get("visual") == "60-hero-turntax-curves":
                poster_idx = round((starts[i] + s.dur * 0.55) * fps)
    for f, frame in render(scenes, fps):
        proc.stdin.write(frame.tobytes())
        if poster_idx is not None and f == poster_idx:
            poster_frame = frame.copy()
        n += 1
        if n % 60 == 0:
            print(f"  ...{n} frames", file=sys.stderr)
    proc.stdin.close()
    proc.wait()
    print(f"wrote {out}  ({n} frames @ {fps}fps = {n/fps:.1f}s)")
    if want_poster:
        if poster_frame is None:
            poster_frame = next(fr for _, fr in render(scenes, fps, frange=(poster_idx or 0, (poster_idx or 0) + 1)))
        poster_frame.save(OUT_POSTER)
        print(f"wrote {OUT_POSTER}")


# A condensed highlight reel for the README GIF: the 5 load-bearing beats only,
# shortened, so the GIF stays small (GitHub markdown can't autoplay an mp4).
GIF_BEATS = {
    "s1_title": 2.0,
    "s3_turntax": 3.0,      # the measured 9.7x
    "s5_capability": 2.6,   # owns the whole boundary
    "s6_statcard": 2.6,     # three pillars + honest fence
    "s9_closing": 2.0,
}


def condense(scene_specs):
    out = []
    for sp in scene_specs:
        if sp["id"] in GIF_BEATS:
            c = dict(sp)
            c["duration_s"] = GIF_BEATS[sp["id"]]
            c["transition_dur_s"] = 0.6
            out.append(c)
    return out


def build_gif(scene_specs, out_gif: Path, fps=24, fps_gif=11, width=760):
    """Render the condensed highlight reel to a temp mp4, then a compact looping GIF."""
    scenes = [Scene(s) for s in condense(scene_specs)]
    tmp = BUILD / "hero-gif-src.mp4"
    write_mp4(scenes, fps, tmp, want_poster=False)
    ff = ffmpeg_exe()
    vf = (f"fps={fps_gif},scale={width}:-1:flags=lanczos,"
          f"split[s0][s1];[s0]palettegen=max_colors=96[p];[s1][p]paletteuse=dither=bayer:bayer_scale=5")
    subprocess.run([ff, "-y", "-i", str(tmp), "-vf", vf, "-loop", "0", str(out_gif)],
                   stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    if out_gif.exists():
        print(f"wrote {out_gif}  ({out_gif.stat().st_size/1e6:.1f} MB)")


# ---------------------------------------------------------------------------
def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--fast", action="store_true", help="15fps draft")
    ap.add_argument("--no-gif", action="store_true")
    ap.add_argument("--crf", type=int, default=26,
                    help="x264 quality (lower=better/bigger); 26 keeps the mp4 < 5MB")
    ap.add_argument("--storyboard", default=str(STORYBOARD))
    ap.add_argument("--out", default=str(OUT_MP4))
    args = ap.parse_args()

    spec = json.loads(Path(args.storyboard).read_text(encoding="utf-8"))
    global W, H
    W = spec.get("resolution", {}).get("w", W)
    H = spec.get("resolution", {}).get("h", H)
    fps = 15 if args.fast else spec.get("fps", 30)

    print(f"building {len(spec['scenes'])} scenes -> {args.out}  ({W}x{H} @{fps}fps)")
    scenes = [Scene(s) for s in spec["scenes"]]
    out = Path(args.out)
    write_mp4(scenes, fps, out, want_poster=True, crf=args.crf)
    if not args.no_gif:
        build_gif(spec["scenes"], OUT_GIF)


if __name__ == "__main__":
    main()
