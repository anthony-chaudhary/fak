    d = ImageDraw.Draw(im)
    cx = W / 2
    y = S(40)
    head = F(SEMIBOLD, S(50))
    y = draw_center(d, cx, y, scene["headline"], head, INK, max_w=S(1740))
    if scene.get("subcaption"):
        y += S(4)
        sub = F(REG, S(28))
        y = draw_center(d, cx, y, scene["subcaption"], sub, GREY, max_w=S(1720))
    d.rectangle([cx - S(34), y + S(10), cx + S(34), y + S(14)], fill=GREEN)
    if scene.get("footer"):
        ff = F(REG, S(22))
        fw = measure(d, scene["footer"], ff)[0]
        d.text((cx - fw / 2, S(1024)), scene["footer"], font=ff, fill=GREY_L)
    bm = F(MONO_B, S(25))
    d.text((S(50), S(1022)), "fak", font=bm, fill=INK)
    bw0 = measure(d, "fak", bm)[0]
    d.rectangle([S(50) + bw0 + S(7), S(1026), S(50) + bw0 + S(15), S(1044)], fill=GREEN)
    return im


