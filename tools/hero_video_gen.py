def find_chrome() -> str | None:
    for c in [
        os.environ.get("CHROME"),
        "C:/Program Files/Google/Chrome/Application/chrome.exe",
        "C:/Program Files (x86)/Microsoft/Edge/Application/msedge.exe",
        "/Applications/Google Chrome.app/Contents/MacOS/Google Chrome",
    ]:
        if c and Path(c).exists():
            return c
    return None


def render_svg(name: str, scale: int = 2) -> Path:
    """Render visuals/<name>.svg -> BUILD/<name>.png at `scale`x via headless Chrome.
    Falls back to an existing visuals/<name>.png if Chrome is missing/fails."""
    out = BUILD / f"{name}.png"
    svg = VISUALS / f"{name}.svg"
    chrome = find_chrome()
    if chrome and svg.exists():
        txt = svg.read_text(encoding="utf-8", errors="ignore")
        import re
        wm = re.search(r'width="(\d+)"', txt)
        hm = re.search(r'height="(\d+)"', txt)
        if wm and hm:
            w, h = int(wm.group(1)), int(hm.group(1))
            cmd = [
                chrome, "--headless", "--disable-gpu", "--hide-scrollbars",
                f"--force-device-scale-factor={scale}",
                "--default-background-color=ffffffff",
                f"--window-size={w},{h}",
                f"--screenshot={out}", str(svg),
            ]
            subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
            if out.exists():
                return out
    # fallback: committed raster
    fallback = VISUALS / f"{name}.png"
    if fallback.exists():
        return fallback
    raise FileNotFoundError(f"cannot obtain raster for {name} (no Chrome render, no {fallback})")


def load_visual(name: str) -> Image.Image:
    return Image.open(render_svg(name)).convert("RGB")


def corner_bg(im: Image.Image) -> tuple[int, int, int]:
    w, h = im.size
    pts = [(3, 3), (w - 4, 3), (3, h - 4), (w - 4, h - 4)]
    cols = [im.getpixel(p) for p in pts]
    # pick the lightest corner (visuals sit on near-white)
    return max(cols, key=lambda c: sum(c))


def autotrim(im: Image.Image, bg: tuple[int, int, int], tol: int = 12,
             pad_frac: float = 0.012) -> Image.Image:
    """Crop the uniform outer margin (within `tol` of `bg`) so the chart fills the
    frame instead of floating inside the SVG's generous canvas padding."""
    bgimg = Image.new("RGB", im.size, bg)
    diff = ImageChops.difference(im, bgimg).convert("L")
    mask = diff.point(lambda p: 255 if p > tol else 0)
    bbox = mask.getbbox()
    if not bbox:
        return im
    x0, y0, x1, y1 = bbox
    pw = round((x1 - x0) * pad_frac)
    ph = round((y1 - y0) * pad_frac)
    return im.crop((max(0, x0 - pw), max(0, y0 - ph),
                    min(im.width, x1 + pw), min(im.height, y1 + ph)))


# ---------------------------------------------------------------------------
# Easing / motion
# ---------------------------------------------------------------------------
def smoothstep(x: float) -> float:
    x = max(0.0, min(1.0, x))
    return x * x * (3 - 2 * x)


def contain(im: Image.Image, bw: int, bh: int) -> Image.Image:
    w, h = im.size
    s = min(bw / w, bh / h)
    return im.resize((max(1, round(w * s)), max(1, round(h * s))), LANCZOS)


def ken_burns(base: Image.Image, bw: int, bh: int, frac: float, motion: str,
              intensity: float, bg: tuple[int, int, int]) -> Image.Image:
    """Return a (bw x bh) frame of `base` (already contained to <= box) with a
    subtle motion applied. `frac` is 0..1 progress within the scene. Any reveal
    is filled with `bg`, which matches the chart's own background, so it is
    seamless."""
    box = Image.new("RGB", (bw, bh), bg)
    e = smoothstep(frac)
    zoom_amp = 0.18 * intensity      # max scale delta
    pan_amp = 0.12 * intensity       # max translate as frac of box

    s = 1.0
    dx = dy = 0.0
    if motion == "zoom_in":
        s = 1.0 + zoom_amp * e
    elif motion == "zoom_out":
        s = 1.0 + zoom_amp * (1 - e)
    elif motion == "reveal":
        s = 1.0 + zoom_amp * (1 - e) * 1.4
    elif motion == "pan_lr":
        s = 1.0 + zoom_amp * 0.5
        dx = (-pan_amp / 2 + pan_amp * e) * bw
    elif motion == "pan_rl":
        s = 1.0 + zoom_amp * 0.5
        dx = (pan_amp / 2 - pan_amp * e) * bw
    elif motion == "pan_down":
        s = 1.0 + zoom_amp * 0.5
        dy = (-pan_amp / 2 + pan_amp * e) * bh
    elif motion == "pan_up":
        s = 1.0 + zoom_amp * 0.5
        dy = (pan_amp / 2 - pan_amp * e) * bh
    else:  # static — a barely-there breath so it is not dead-still
        s = 1.0 + 0.01 * intensity * e

    cw, ch = base.size
    nw, nh = max(1, round(cw * s)), max(1, round(ch * s))
    cur = base.resize((nw, nh), LANCZOS) if (nw, nh) != (cw, ch) else base
    ox = round((bw - nw) / 2 + dx)
    oy = round((bh - nh) / 2 + dy)
    box.paste(cur, (ox, oy))
    return box


# ---------------------------------------------------------------------------
# Text helpers
# ---------------------------------------------------------------------------
def measure(draw, text, fnt):
    b = draw.textbbox((0, 0), text, font=fnt)
    return b[2] - b[0], b[3] - b[1]


def wrap(draw, text, fnt, max_w):
    words = text.split()
    lines, cur = [], ""
    for w_ in words:
        trial = (cur + " " + w_).strip()
        if measure(draw, trial, fnt)[0] <= max_w or not cur:
            cur = trial
        else:
            lines.append(cur)
            cur = w_
    if cur:
        lines.append(cur)
    return lines


def draw_center(draw, cx, y, text, fnt, fill, max_w=None):
    if max_w:
        lines = wrap(draw, text, fnt, max_w)
    else:
        lines = [text]
    asc, desc = fnt.getmetrics()
    lh = asc + desc + 8
    for i, ln in enumerate(lines):
        tw = measure(draw, ln, fnt)[0]
        draw.text((cx - tw / 2, y + i * lh), ln, font=fnt, fill=fill)
    return y + len(lines) * lh


def draw_left(draw, x, y, text, fnt, fill, max_w=None):
    lines = wrap(draw, text, fnt, max_w) if max_w else [text]
    asc, desc = fnt.getmetrics()
    lh = asc + desc + 8
    for i, ln in enumerate(lines):
        draw.text((x, y + i * lh), ln, font=fnt, fill=fill)
    return y + len(lines) * lh


