# ---------------------------------------------------------------------------
# Animated turn-tax renderer — drawn frame-by-frame from the data file.
# This is the HERO: axes steady, curves sweep L->R, gap shades, multiplier counts up.
# ---------------------------------------------------------------------------
def _fmt_mult(target_str: str, frac: float) -> str:
    """Count a multiplier string up to its target over `frac` (0..1).
    Preserves the trailing unit ('×' / '%') and thousands separators."""
    t = target_str.strip()
    unit = ""
    if t.endswith("×") or t.endswith("x"):
        unit = "×"
        t = t[:-1]
    elif t.endswith("%"):
        unit = "%"
        t = t[:-1]
    num = float(t.replace(",", ""))
    cur = num * ease_out_cubic(frac)
    if num >= 1000:
        return f"{cur:,.0f}{unit}"
    if num >= 100:
        return f"{cur:,.0f}{unit}"
    # one decimal for the small multipliers (9.7×, 4.1×)
    return f"{cur:,.1f}{unit}"


class TurnTaxAnim:
    """Holds the parsed turn-tax data and renders the chart layer at a reveal frac."""

    def __init__(self):
        data = json.loads((TOOLS / "hero_turntax.data.json").read_text(encoding="utf-8"))
        self.panels = data["panels"]
        self.legend = data["legend"]

    def render(self, bw: int, bh: int, frac: float, bg) -> Image.Image:
        im = Image.new("RGB", (bw, bh), bg)
        d = ImageDraw.Draw(im)
        n = len(self.panels)
        gap = S(34)
        pw = (bw - gap * (n - 1)) // n
        # reveal is global; each panel sweeps with a small stagger so it reads as a build
        for i, p in enumerate(self.panels):
            px = i * (pw + gap)
            stagger = 0.10 * i
            pf = max(0.0, min(1.0, (frac - stagger) / max(1e-6, 1 - 0.10 * (n - 1))))
            self._panel(d, im, px, 0, pw, bh, p, pf)
        return im

    def _panel(self, d, im, x, y, pw, ph, p, pf):
        # panel chrome
        title = F(SEMIBOLD, S(30))
        subt = F(REG, S(18))
        d.text((x, y + S(2)), p["title"], font=title, fill=INK)
        d.text((x, y + S(40)), p["subtitle"], font=subt, fill=GREY)

        # plot rect inside the panel
        plot_x0 = x + S(14)
        plot_x1 = x + pw - S(10)
        plot_y0 = y + S(96)
        plot_y1 = y + ph - S(74)
        pw2 = plot_x1 - plot_x0
        ph2 = plot_y1 - plot_y0

        x_vals = p["x_vals"]
        xmin, xmax = min(x_vals), max(x_vals)
        y_max = float(p["y_max"])

        def sx(v):
            return plot_x0 + (v - xmin) / (xmax - xmin) * pw2

        def sy(v):
            return plot_y1 - (v / y_max) * ph2

        # gridlines + y ticks (steady)
        gf = F(REG, S(16))
        for yt in p["y_ticks"]:
            yy = sy(yt["v"])
            d.line([(plot_x0, yy), (plot_x1, yy)], fill=GRID, width=max(1, S(1)))
            d.text((plot_x0 + S(2), yy - S(20)), yt["t"], font=gf, fill=GREY_L)
        # x axis
        d.line([(plot_x0, plot_y1), (plot_x1, plot_y1)], fill=HAIRLINE, width=max(1, S(2)))
        for xt, xv in zip(p["x_ticks"], x_vals):
            xx = sx(xv)
            tw = measure(d, xt, gf)[0]
            d.text((xx - tw / 2, plot_y1 + S(8)), xt, font=gf, fill=GREY_L)

        # build the two polylines, revealed up to `pf` along the x extent
        base = p["baseline"]
        fak = p["fak"]
        # reveal as a continuous x position so the line grows smoothly, not point-by-point
        reveal_v = xmin + (xmax - xmin) * ease_out_cubic(pf)

        def clipped_line(vals, reveal):
            pts = []
            for xv, yv in zip(x_vals, vals):
                if xv <= reveal:
                    pts.append((sx(xv), sy(yv)))
                else:
                    # interpolate the partial segment to the reveal x
                    # find the segment this reveal falls in
                    prev_i = max(0, [j for j, xx in enumerate(x_vals) if xx <= reveal][-1]
                                 if any(xx <= reveal for xx in x_vals) else 0)
                    if prev_i < len(x_vals) - 1:
                        x0v, x1v = x_vals[prev_i], x_vals[prev_i + 1]
                        y0v, y1v = vals[prev_i], vals[prev_i + 1]
                        if x1v != x0v and x0v <= reveal <= x1v:
                            t = (reveal - x0v) / (x1v - x0v)
                            yy = y0v + (y1v - y0v) * t
                            pts.append((sx(reveal), sy(yy)))
                    break
            return pts

        base_pts = clipped_line(base, reveal_v)
        fak_pts = clipped_line(fak, reveal_v)

        # shade the gap between baseline and fak up to the reveal
        if len(base_pts) >= 2 and len(fak_pts) >= 2:
            poly = base_pts + list(reversed(fak_pts))
            shade = Image.new("RGBA", im.size, (0, 0, 0, 0))
            sd = ImageDraw.Draw(shade)
            sd.polygon(poly, fill=(46, 139, 87, 34))
            im.paste(Image.alpha_composite(im.convert("RGBA"), shade).convert("RGB"), (0, 0))
            d2 = ImageDraw.Draw(im)
        else:
            d2 = d

        lw = max(2, S(4))
        if len(base_pts) >= 2:
            d2.line(base_pts, fill=BLUE, width=lw, joint="curve")
        if len(fak_pts) >= 2:
            d2.line(fak_pts, fill=GREEN, width=lw, joint="curve")
        # endpoint dots once fully revealed
        if pf > 0.985:
            r = max(2, S(5))
            bx, by = sx(x_vals[-1]), sy(base[-1])
            fx, fy = sx(x_vals[-1]), sy(fak[-1])
            d2.ellipse([bx - r, by - r, bx + r, by + r], fill=BLUE)
            d2.ellipse([fx - r, fy - r, fx + r, fy + r], fill=GREEN)

        # the big multiplier, counting up. Sits in the empty lower-centre wedge
        # (below the steep baseline, above the flat fak line) with a soft halo in
        # the card background so it stays legible wherever a curve passes near.
        mult_f = F(MONO_B, S(58))
        mtxt = _fmt_mult(p["mult"], pf)
        mtw, mth = measure(d2, mtxt, mult_f)
        mcx = plot_x0 + pw2 * 0.5
        mcy = plot_y1 - ph2 * 0.30
        subf = F(SEMIBOLD, S(20))
        stw = measure(d2, p["mult_sub"], subf)[0]
        sub_x = mcx - stw / 2
        sub_y = mcy + mth / 2 + S(6)

        def halo(cx_, top_, txt_, fnt_, w_):
            r = max(2, S(4))
            for ox in (-r, 0, r):
                for oy in (-r, 0, r):
                    d2.text((cx_ - w_ / 2 + ox, top_ + oy), txt_, font=fnt_, fill=CARD_BG)

        halo(mcx, mcy - mth / 2, mtxt, mult_f, mtw)
        d2.text((mcx - mtw / 2, mcy - mth / 2), mtxt, font=mult_f, fill=GREEN_D)
        if pf > 0.55:
            halo(mcx, sub_y, p["mult_sub"], subf, stw)
            d2.text((sub_x, sub_y), p["mult_sub"], font=subf, fill=INK2)
        # panel footnote — one line, ellipsised, kept clear of the x tick labels
        if pf > 0.85:
            footf = F(REG, S(15))
            ftxt = wrap(d2, p["mult_foot"], footf, pw2)
            line = ftxt[0] if ftxt else ""
            if len(ftxt) > 1:
                line = line.rstrip(" .,—-") + " …"
            d2.text((plot_x0, plot_y1 + S(40)), line, font=footf, fill=GREY_L)


