def render_svg(name: str, scale: int = 3) -> Path:
    """Obtain a high-res raster for visuals/<name>.

    Hand-authored cards carry a numeric root width (e.g. width="1200") and
    rasterize crisply through headless Chrome. Mermaid decks instead emit
    width="100%" + a max-width style, which headless Chrome sizes unreliably
    (it can collapse to a tiny strip) — for those the committed visuals/<name>.png
    IS the canonical "always-visible" render (text baked in), so we use it
    directly. See visuals/RENDERING-NOTE.md."""
    out = BUILD / f"{name}@{scale}.png"
    if out.exists():
        return out
    svg = VISUALS / f"{name}.svg"
    fallback = VISUALS / f"{name}.png"
    txt = svg.read_text(encoding="utf-8", errors="ignore") if svg.exists() else ""
    import re
    root = re.search(r"<svg[^>]*>", txt)
    # numeric only if the root width is digits closed by a quote (width="1200"),
    # NOT a percentage (width="100%") — the "1" in "100%" must not count.
    numeric_root = bool(root and re.search(r'\bwidth="\d[\d.]*"', root.group(0)))

    # Mermaid (width="100%") -> prefer the canonical committed PNG.
    if not numeric_root and fallback.exists():
        return fallback

    chrome = find_chrome()
    if chrome and numeric_root:
        wm = re.search(r'\bwidth="(\d+(?:\.\d+)?)"', root.group(0))
        hm = re.search(r'\bheight="(\d+(?:\.\d+)?)"', root.group(0))
        w = round(float(wm.group(1))) if wm else None
        h = round(float(hm.group(1))) if hm else None
        if not (w and h):
            vb = re.search(r'viewBox="\s*[-\d.]+\s+[-\d.]+\s+([\d.]+)\s+([\d.]+)', txt)
            if vb:
                w, h = round(float(vb.group(1))), round(float(vb.group(2)))
        if w and h:
            cmd = [
                chrome, "--headless", "--disable-gpu", "--hide-scrollbars",
                f"--force-device-scale-factor={scale}",
                "--default-background-color=ffffffff",
                f"--window-size={w},{h}",
                f"--screenshot={out}", str(svg),
            ]
            subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
            if out.exists():
                return out
    # last resort: committed raster, else a Chrome render of whatever we have
    if fallback.exists():
        return fallback
    raise FileNotFoundError(f"cannot obtain raster for {name} (no Chrome render, no {fallback})")


def load_visual(name: str, scale: int = 3) -> Image.Image:
    return Image.open(render_svg(name, scale)).convert("RGB")


def corner_bg(im: Image.Image) -> tuple[int, int, int]:
    w, h = im.size
    pts = [(3, 3), (w - 4, 3), (3, h - 4), (w - 4, h - 4)]
    cols = [im.getpixel(p) for p in pts]
    # pick the lightest corner (visuals sit on near-white)
    return max(cols, key=lambda c: sum(c))


def autotrim(im: Image.Image, bg: tuple[int, int, int], tol: int = 12,
             pad_frac: float = 0.012) -> Image.Image:
    """Crop the uniform outer margin (within `tol` of `bg`) so the chart fills the
    frame instead of floating inside the SVG's generous canvas padding."""
    bgimg = Image.new("RGB", im.size, bg)
    diff = ImageChops.difference(im, bgimg).convert("L")
    mask = diff.point(lambda p: 255 if p > tol else 0)
    bbox = mask.getbbox()
    if not bbox:
        return im
    x0, y0, x1, y1 = bbox
    pw = round((x1 - x0) * pad_frac)
    ph = round((y1 - y0) * pad_frac)
    return im.crop((max(0, x0 - pw), max(0, y0 - ph),
                    min(im.width, x1 + pw), min(im.height, y1 + ph)))


# ---------------------------------------------------------------------------
# Easing / motion
# ---------------------------------------------------------------------------
def smoothstep(x: float) -> float:
    x = max(0.0, min(1.0, x))
    return x * x * (3 - 2 * x)


def ease_out_cubic(x: float) -> float:
    x = max(0.0, min(1.0, x))
    return 1 - (1 - x) ** 3


def contain(im: Image.Image, bw: int, bh: int) -> Image.Image:
    w, h = im.size
    s = min(bw / w, bh / h)
    return im.resize((max(1, round(w * s)), max(1, round(h * s))), LANCZOS)


def ken_burns(base: Image.Image, bw: int, bh: int, frac: float, motion: str,
              intensity: float, bg: tuple[int, int, int]) -> Image.Image:
    """Return a (bw x bh) frame of `base` (already contained to <= box) with a
    subtle motion applied. `frac` is 0..1 progress within the scene."""
    box = Image.new("RGB", (bw, bh), bg)
    e = smoothstep(frac)
    zoom_amp = 0.16 * intensity      # max scale delta
    pan_amp = 0.10 * intensity       # max translate as frac of box

    s = 1.0
    dx = dy = 0.0
    if motion == "zoom_in":
        s = 1.0 + zoom_amp * e
    elif motion == "zoom_out":
        s = 1.0 + zoom_amp * (1 - e)
    elif motion == "reveal":
        s = 1.0 + zoom_amp * (1 - e) * 1.4
    elif motion == "pan_lr":
        s = 1.0 + zoom_amp * 0.5
        dx = (-pan_amp / 2 + pan_amp * e) * bw
    elif motion == "pan_rl":
        s = 1.0 + zoom_amp * 0.5
        dx = (pan_amp / 2 - pan_amp * e) * bw
    elif motion == "pan_down":
        s = 1.0 + zoom_amp * 0.5
        dy = (-pan_amp / 2 + pan_amp * e) * bh
    elif motion == "pan_up":
        s = 1.0 + zoom_amp * 0.5
        dy = (pan_amp / 2 - pan_amp * e) * bh
    else:  # static — a barely-there breath so it is not dead-still
        s = 1.0 + 0.01 * intensity * e

    cw, ch = base.size
    nw, nh = max(1, round(cw * s)), max(1, round(ch * s))
    cur = base.resize((nw, nh), LANCZOS) if (nw, nh) != (cw, ch) else base
    ox = round((bw - nw) / 2 + dx)
    oy = round((bh - nh) / 2 + dy)
    box.paste(cur, (ox, oy))
    return box


def reveal_wipe(base: Image.Image, bw: int, bh: int, frac: float,
                bg: tuple[int, int, int], settle: float = 0.62) -> Image.Image:
    """Build a steady raster IN: a soft left-to-right reveal of the chart over the
    first `settle` of the scene, then a gentle settle-zoom. Makes a dense card feel
    composed rather than pasted, without re-drawing its layout."""
    box = Image.new("RGB", (bw, bh), bg)
    # late-scene settle: a tiny zoom so it is alive after the wipe completes
    e_settle = smoothstep(max(0.0, (frac - settle) / max(1e-6, 1 - settle)))
    s = 1.0 + 0.05 * e_settle
    cw, ch = base.size
    nw, nh = max(1, round(cw * s)), max(1, round(ch * s))
    cur = base.resize((nw, nh), LANCZOS) if (nw, nh) != (cw, ch) else base
    ox = round((bw - nw) / 2)
    oy = round((bh - nh) / 2)

    # reveal fraction with a soft feathered leading edge
    rp = ease_out_cubic(min(1.0, frac / settle)) if settle > 0 else 1.0
    if rp >= 0.999:
        box.paste(cur, (ox, oy))
        return box
    reveal_x = int(nw * rp)
    feather = max(2, int(nw * 0.05))
    # paste the revealed portion fully
    if reveal_x > 0:
        crop = cur.crop((0, 0, min(nw, reveal_x), nh))
        box.paste(crop, (ox, oy))
    # feathered edge: blend a narrow band so the wipe edge is soft, not a hard line
    fx0 = max(0, reveal_x - feather)
    if reveal_x < nw and fx0 < nw:
        band = cur.crop((fx0, 0, min(nw, reveal_x + feather), nh))
        bandbox = Image.new("RGB", band.size, bg)
        # linear alpha across the band, 1 at the left, 0 at the right
        bw2 = band.size[0]
        alpha = Image.new("L", band.size, 0)
        adraw = ImageDraw.Draw(alpha)
        for i in range(bw2):
            a = int(255 * max(0.0, 1.0 - i / max(1, bw2)))
            adraw.line([(i, 0), (i, band.size[1])], fill=a)
        merged = Image.composite(band, bandbox, alpha)
        box.paste(merged, (ox + fx0, oy))
    return box


def scroll_progress(frac: float, hold: float) -> float:
    """0..1 scroll position with a static hold at each end so the viewer can read
    the top of the diagram before the pan starts and the bottom after it lands."""
    if frac <= hold:
        return 0.0
    if frac >= 1 - hold:
        return 1.0
    return smoothstep((frac - hold) / max(1e-6, 1 - 2 * hold))


def reveal_scroll(base: Image.Image, bw: int, bh: int, frac: float,
                  bg: tuple[int, int, int], axis: str, hold: float = 0.13) -> Image.Image:
    """Scale a tall (axis='v') or wide (axis='h') diagram to FILL the box on its
    short axis, then pan it fully along its long axis — a steady scroll that reads
    the whole diagram top-to-bottom (or left-to-right) instead of letterboxing it
    into a thin strip. Holds at both ends so the start and end frames are legible."""
    box = Image.new("RGB", (bw, bh), bg)
    iw, ih = base.size
    p = scroll_progress(frac, hold)
    if axis == "v":
        s = bw / iw
        nw, nh = bw, max(1, round(ih * s))
        cur = base.resize((nw, nh), LANCZOS)
        travel = max(0, nh - bh)
        box.paste(cur, (0, -round(travel * p)))
    else:  # 'h'
        s = bh / ih
        nw, nh = max(1, round(iw * s)), bh
        cur = base.resize((nw, nh), LANCZOS)
        travel = max(0, nw - bw)
        box.paste(cur, (-round(travel * p), 0))
    return box


