def ffmpeg_exe():
    import imageio_ffmpeg
    return imageio_ffmpeg.get_ffmpeg_exe()


def write_mp4(scenes, fps, out: Path, want_poster=False, crf=19, poster: Path = OUT_POSTER):
    ff = ffmpeg_exe()
    cmd = [
        ff, "-y", "-f", "rawvideo", "-pix_fmt", "rgb24",
        "-s", f"{W}x{H}", "-r", str(fps), "-i", "-",
        "-an", "-c:v", "libx264", "-pix_fmt", "yuv420p",
        "-crf", str(crf), "-preset", "slow", "-profile:v", "high",
        "-movflags", "+faststart", str(out),
    ]
    proc = subprocess.Popen(cmd, stdin=subprocess.PIPE, stdout=subprocess.DEVNULL,
                            stderr=subprocess.DEVNULL)
    n = 0
    poster_frame = None
    starts, ends = build_timeline(scenes)
    poster_idx = None
    if want_poster:
        # prefer a scene flagged "poster": true; else the turn-tax count-up beat.
        cands = ([i for i, s in enumerate(scenes) if s.spec.get("poster")] or
                 [i for i, s in enumerate(scenes)
                  if s.spec.get("visual") == "60-hero-turntax-curves"])
        if cands:
            i = cands[0]
            s = scenes[i]
            # land on the scene's final readable beat, but BEFORE the cross-dissolve
            # into the next scene starts (so no ghost bleed-through)
            nxt_tin = scenes[i + 1].tin if i + 1 < len(scenes) else 0.0
            poster_t = starts[i] + s.dur - nxt_tin - 0.15
            poster_idx = round(poster_t * fps)
    for f, frame in render(scenes, fps):
        proc.stdin.write(frame.tobytes())
        if poster_idx is not None and f == poster_idx:
            poster_frame = frame.copy()
        n += 1
        if n % 60 == 0:
            print(f"  ...{n} frames", file=sys.stderr)
    proc.stdin.close()
    proc.wait()
    print(f"wrote {out}  ({n} frames @ {fps}fps = {n/fps:.1f}s)")
    if want_poster:
        if poster_frame is None:
            poster_frame = next(fr for _, fr in render(scenes, fps, frange=(poster_idx or 0, (poster_idx or 0) + 1)))
        poster_frame.save(poster)
        print(f"wrote {poster}")


# A condensed highlight reel for the README GIF: the load-bearing beats only,
# shortened, so the GIF stays small (GitHub markdown can't autoplay an mp4).
GIF_BEATS = {
    "s1_title": 1.8,
    "s2_turntax": 3.4,      # the HERO — measured 9.7x sweep + count-up
    "s5_capability": 2.4,   # owns the whole boundary
    "s6_statcard": 2.4,     # three pillars + honest fence
    "s9_closing": 1.8,
}


def condense(scene_specs):
    # A storyboard can mark its own GIF beats with "gif": true (+ optional
    # "gif_dur"); otherwise fall back to the hero deck's hardcoded beat list.
    flagged = [sp for sp in scene_specs if sp.get("gif")]
    out = []
    if flagged:
        for sp in flagged:
            c = dict(sp)
            c["duration_s"] = float(sp.get("gif_dur", 2.4))
            c["transition_dur_s"] = 0.5
            out.append(c)
        return out
    for sp in scene_specs:
        if sp["id"] in GIF_BEATS:
            c = dict(sp)
            c["duration_s"] = GIF_BEATS[sp["id"]]
            c["transition_dur_s"] = 0.55
            out.append(c)
    return out


def build_gif(scene_specs, out_gif: Path, fps=24, fps_gif=12, width=820):
    """Render the condensed highlight reel to a temp mp4, then a compact looping GIF."""
    scenes = [Scene(s) for s in condense(scene_specs)]
    tmp = BUILD / "hero-gif-src.mp4"
    write_mp4(scenes, fps, tmp, want_poster=False)
    ff = ffmpeg_exe()
    vf = (f"fps={fps_gif},scale={width}:-1:flags=lanczos,"
          f"split[s0][s1];[s0]palettegen=max_colors=96[p];[s1][p]paletteuse=dither=bayer:bayer_scale=5")
    subprocess.run([ff, "-y", "-i", str(tmp), "-vf", vf, "-loop", "0", str(out_gif)],
                   stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    if out_gif.exists():
        print(f"wrote {out_gif}  ({out_gif.stat().st_size/1e6:.1f} MB)")


